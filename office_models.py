def get_employee_name_uk(employee_type: str) -> str:
    """Повертає українську назву працівника за його типом"""
    employee = next((e for e in EMPLOYEE_TYPES if e['type'] == employee_type), None)
    return employee['name_uk'] if employee else employee_type

def get_employee_by_type(employee_type: str):
    """Повертає інформацію про працівника за типом"""
    return next((e for e in EMPLOYEE_TYPES if e['type'] == employee_type), None)

def get_employee_daily_profit(employee_type: str) -> float:
    """Повертає щоденний прибуток працівника"""
    employee = get_employee_by_type(employee_type)
    return employee['daily_profit'] if employee else 0.0

def get_employee_price(employee_type: str) -> float:
    """Повертає ціну працівника"""
    employee = get_employee_by_type(employee_type)
    return employee['price_uah'] if employee else 0.0

def get_employee_max_count(employee_type: str) -> int:
    """Повертає максимальное количество работников этого типа"""
    employee = get_employee_by_type(employee_type)
    return employee.get('max_count', 1) if employee else 1

